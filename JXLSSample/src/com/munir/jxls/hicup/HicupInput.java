package com.munir.jxls.hicup;

public class HicupInput {

	private long counts;
	private double let;
	private int angle;
	private double xsect;
	
	public long getCounts() {
		return counts;
	}
	public void setCounts(long counts) {
		this.counts = counts;
	}
	public double getLet() {
		return let;
	}
	public void setLet(double let) {
		this.let = let;
	}
	public int getAngle() {
		return angle;
	}
	public void setAngle(int angle) {
		this.angle = angle;
	}
	public double getXsect() {
		return xsect;
	}
	public void setXsect(double xsect) {
		this.xsect = xsect;
	}
	
	
		
		
}
