package com.munir.jxls.hicup;

public class HicupOutput {

	private double onset;
	private double wl;
	private double pwr;
	private double viewFactor;
	private double asymptote;
	private double funnel;
	private String fitCriteria;
	
	public double getOnset() {
		return onset;
	}
	public void setOnset(double onset) {
		this.onset = onset;
	}
	public double getWl() {
		return wl;
	}
	public void setWl(double wl) {
		this.wl = wl;
	}
	public double getPwr() {
		return pwr;
	}
	public void setPwr(double pwr) {
		this.pwr = pwr;
	}
	public double getViewFactor() {
		return viewFactor;
	}
	public void setViewFactor(double viewFactor) {
		this.viewFactor = viewFactor;
	}
	public double getAsymptote() {
		return asymptote;
	}
	public void setAsymptote(double asymptote) {
		this.asymptote = asymptote;
	}
	public double getFunnel() {
		return funnel;
	}
	public void setFunnel(double funnel) {
		this.funnel = funnel;
	}
	public String getFitCriteria() {
		return fitCriteria;
	}
	public void setFitCriteria(String fitCriteria) {
		this.fitCriteria = fitCriteria;
	}		
}
